package de.erethon.factions.poll;

/**
 * @author Fyreum
 */
public enum PollScope {

    /**
     * Only faction admins can participate in the vote.
     */
    ADMIN,
    /**
     * Only faction mods can participate in the vote.
     */
    MOD,
    /**
     * Every faction member can participate in the vote.
     */
    MEMBER,
    /**
     * Every player can participate in the vote.
     */
    PLAYER,

}

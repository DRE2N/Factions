package de.erethon.factions.region;

/**
 * @author Fyreum
 */
public enum ChunkOperation {

    /**
     * Adds the current chunk to the region.
     */
    ADD,
    /**
     * Nothing happens => idle.
     */
    IDLE,
    /**
     * Removes the current chunk from the region.
     */
    REMOVE,

}
